package com.dl.client.core;

public class ServerNetNode {
	private String ip;
	private int port;
	
	public ServerNetNode() {
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
}
